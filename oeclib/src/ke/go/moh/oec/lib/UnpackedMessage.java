package ke.go.moh.oec.lib;

/**
 * This is the information that results when an OpenEMRConnect XML message
 * is unpacked.
 *
 * @author Jim Grace
 */
class UnpackedMessage {
    /** The data form the message. Object type depends on message type. */
    private Object returnData;
    /** A reference to a message type structure containing information about the message type. */
    private MessageType messageType;
    /** The address of the original source of this message. */
    private String source;
    /** The message ID of this message. */
    private String messageId;

    public MessageType getMessageType() {
        return messageType;
    }

    public void setMessageType(MessageType messageType) {
        this.messageType = messageType;
    }

    public String getMessageId() {
        return messageId;
    }

    public void setMessageId(String messageId) {
        this.messageId = messageId;
    }

    public Object getReturnData() {
        return returnData;
    }

    public void setReturnData(Object returnData) {
        this.returnData = returnData;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

}
